public class Tamagotchi {
  String mName;
  Integer mFoodLevel;
  Integer mSleepLevel;
  Integer mActivity;
  Integer mHealth;

  public Tamagotchi(String name, Integer foodLevel, Integer sleepLevel, Integer activity, Integer health) {
    mName = name;
    mFoodLevel = foodLevel;
    mSleepLevel = sleepLevel;
    mActivity = activity;
    mHealth = health;
  }
}
